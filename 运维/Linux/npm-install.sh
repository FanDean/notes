# 使用 npm install -g 安装的包（全局安装）

# 这里 node 是直接下载node.js安装包并解压到/opt目录进行安装的，
# 所以运行使用npm进行全局安装时无需使用管理员权限。
# 你也可以在 ~/.npmrc 文件中更改全局安装目录

node --version
npm --version

# 更新现有node

# 升级现有npm
npm install npm -g


# Gitmoji is an emoji guide for GitHub commit messages.
# 安装后的命令为：gitmoji ，用法见其帮助。
npm i gitmoji-cli -g


# 安装CSS预编译器 Less
npm i less -g


